import React from 'react';
import { NavLink, useNavigate } from 'react-router-dom';
import { Meteor } from 'meteor/meteor';
import { useLoggedUser } from 'meteor/quave:logged-user-react';
import { RoutePaths } from '../general/RoutePaths';

const NAV_ITEMS = [
  { path: RoutePaths.ADMIN_DASHBOARD, label: 'Dashboard', icon: DashboardIcon },
  { path: RoutePaths.ADMIN_USERS, label: 'Users', icon: UsersIcon },
  { path: RoutePaths.ADMIN_GROUPS, label: 'Groups', icon: GroupsIcon },
  { path: RoutePaths.ADMIN_DOMAIN, label: 'Domain', icon: DomainIcon },
  { path: RoutePaths.ADMIN_SETTINGS, label: 'Settings', icon: SettingsIcon },
];

export function AdminLayout({ children }) {
  const { loggedUser } = useLoggedUser();
  const navigate = useNavigate();

  function handleLogout() {
    Meteor.logout(() => {
      navigate(RoutePaths.LOGIN);
    });
  }

  const displayName = loggedUser?.profile?.displayName || loggedUser?.username || '';

  return (
    <div className="flex h-screen bg-gray-950">
      {/* Sidebar */}
      <aside className="flex w-64 flex-col bg-gray-900 border-r border-gray-800">
        {/* Logo */}
        <div className="flex h-16 items-center px-6 border-b border-gray-800">
          <h1 className="text-lg font-bold text-white">Samba Conductor</h1>
        </div>

        {/* Navigation */}
        <nav className="flex-1 space-y-1 px-3 py-4">
          {NAV_ITEMS.map((item) => (
            <NavLink
              key={item.path}
              to={item.path}
              end={item.path === RoutePaths.ADMIN_DASHBOARD}
              className={({ isActive }) =>
                `flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors ${
                  isActive
                    ? 'bg-blue-600 text-white'
                    : 'text-gray-400 hover:bg-gray-800 hover:text-white'
                }`
              }
            >
              <item.icon />
              {item.label}
            </NavLink>
          ))}
        </nav>

        {/* User / Logout */}
        <div className="border-t border-gray-800 p-4">
          <div className="flex items-center justify-between">
            <div className="min-w-0">
              <p className="truncate text-sm font-medium text-white">{displayName}</p>
              <p className="text-xs text-gray-500">
                {loggedUser?.profile?.isAdmin ? 'Administrator' : 'User'}
              </p>
            </div>
            <button
              onClick={handleLogout}
              className="rounded-lg p-2 text-gray-400 hover:bg-gray-800 hover:text-white transition-colors"
              title="Sign out"
            >
              <LogoutIcon />
            </button>
          </div>
        </div>
      </aside>

      {/* Main content */}
      <main className="flex-1 overflow-auto">
        <div className="p-6 lg:p-8">
          {children}
        </div>
      </main>
    </div>
  );
}

